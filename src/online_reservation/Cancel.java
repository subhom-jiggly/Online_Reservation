    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        int a = Integer.parseInt(pnrentry.getText());
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/onlinereservationsystem", "root", "mysqlserver");
            Statement stm = con.createStatement();
            String sql = "SELECT * FROM booking WHERE pnr=" + a;
            ResultSet rs = stm.executeQuery(sql);
            if (rs.next()) {
                name.setText(rs.getString("name"));
                fromplace.setText(rs.getString("Fromloc"));
                toplace.setText(rs.getString("Toloc"));
                trainname.setText(rs.getString("trainname"));
                deptime.setText(rs.getString("dep"));
                dateofjourney.setText(rs.getString("dateoftravel"));
                classoftravel.setText(rs.getString("class"));
            } else {
                JOptionPane.showMessageDialog(this, "Register yourself first");
                this.dispose();
                new Register().setVisible(true);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            System.out.print(ex);
        } catch (Exception e) {
            System.out.print(e);
        }
    }//GEN-LAST:event_searchActionPerformed

