    private void cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelActionPerformed
        int box = JOptionPane.showConfirmDialog(null, "Are you sure to cancel booking?");
         int a = Integer.parseInt(pnrentry.getText());
        if (box == JOptionPane.YES_OPTION) {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/onlinereservationsystem", "root", "mysqlserver");
                Statement stm = con.createStatement();
                String sql="DELETE FROM booking WHERE pnr="+a;
                stm.executeUpdate(sql);
                JOptionPane.showMessageDialog(this,"Your Booking canceled via PNR: "+a);
                this.dispose();
            } catch (ClassNotFoundException | SQLException ex) {
                System.out.print(ex);
            } catch (Exception e) {
                System.out.print(e);
            }
        }
        else
        {
            this.dispose();
            new Home().setVisible(true);
        }
    }//GEN-LAST:event_cancelActionPerformed

